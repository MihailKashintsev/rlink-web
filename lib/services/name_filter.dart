/// Anti-bullying name filter for Ether.
/// Contains a comprehensive dictionary of Russian names (full, short, diminutive)
/// and common English names. Any ether message containing a name is blocked/hidden.
class NameFilter {
  NameFilter._();
  static final instance = NameFilter._();

  /// Extra names from contacts / profile — updated at runtime.
  final Set<String> _dynamicNames = {};

  void updateDynamicNames(Set<String> names) {
    _dynamicNames.clear();
    for (final n in names) {
      final low = n.toLowerCase().trim();
      if (low.length >= 2) _dynamicNames.add(low);
    }
  }

  /// Returns the detected name if [text] contains any known name, or null.
  /// Also checks for profanity/insults via [detectBadWord].
  String? detect(String text) {
    if (text.length < 2) return null;
    final lower = text.toLowerCase();

    // 1. Bad words / profanity check
    for (final w in _kBadWords) {
      if (_wordMatch(lower, w)) return w;
    }

    // 2. Dynamic names (contacts + own profile)
    for (final n in _dynamicNames) {
      if (_wordMatch(lower, n)) return n;
    }

    // 3. Static dictionary
    for (final n in _kAllNames) {
      if (_wordMatch(lower, n)) return n;
    }

    return null;
  }

  /// Returns the detected profanity/insult word if [text] contains one, or null.
  String? detectBadWord(String text) {
    if (text.length < 2) return null;
    final lower = text.toLowerCase();
    for (final w in _kBadWords) {
      if (_wordMatch(lower, w)) return w;
    }
    return null;
  }

  /// Word-boundary-aware match: the name must appear as a standalone token,
  /// not as a substring of a longer word (e.g. "масло" should not match "мас").
  /// We allow the name to be at the start/end of string or bordered by non-letter chars.
  bool _wordMatch(String text, String name) {
    int start = 0;
    while (true) {
      final idx = text.indexOf(name, start);
      if (idx < 0) return false;

      final before = idx > 0 ? text.codeUnitAt(idx - 1) : 0x20;
      final afterIdx = idx + name.length;
      final after = afterIdx < text.length ? text.codeUnitAt(afterIdx) : 0x20;

      // Accept if both sides are non-letter (space, punctuation, emoji, etc.)
      if (!_isLetter(before) && !_isLetter(after)) return true;

      start = idx + 1;
    }
  }

  bool _isLetter(int c) {
    // Basic Latin letters
    if (c >= 0x41 && c <= 0x5A) return true; // A-Z
    if (c >= 0x61 && c <= 0x7A) return true; // a-z
    // Cyrillic
    if (c >= 0x0410 && c <= 0x044F) return true; // А-я
    if (c == 0x0401 || c == 0x0451) return true; // Ё ё
    return false;
  }
}

